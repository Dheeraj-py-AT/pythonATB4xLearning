# Program to find the Max number between two integers.

print(max(44, 577, 1000))
# 1000

print(max(10, 23, 45, -1, -2, 1, 87.34))
# 87.34

input_1 = 44
input_2 = 49
print(max(input_1, input_2))
# 49

